<?php
declare(strict_types=1);

namespace Tests\TestCase\DB\Schema\Postgres;

use Fyre\Cache\CacheManager;
use Fyre\Cache\Cacher;
use Fyre\Cache\Handlers\FileCacher;
use Fyre\Core\Container;
use Fyre\DB\Connection;
use Fyre\DB\ConnectionManager;
use Fyre\DB\Handlers\Postgres\PostgresConnection;
use Fyre\DB\Schema\Schema;
use Fyre\DB\Schema\SchemaRegistry;
use Fyre\DB\TypeParser;
use Override;

use function getenv;
use function mkdir;
use function rmdir;
use function unlink;

trait PostgresConnectionTrait
{
    protected Cacher $cache;

    protected Connection $db;

    protected Schema $schema;

    #[Override]
    protected function setUp(): void
    {
        $container = new Container();
        $container->singleton(TypeParser::class);
        $container->singleton(CacheManager::class);
        $container->use(CacheManager::class)->setConfig('_schema', [
            'className' => FileCacher::class,
            'path' => 'tmp',
            'prefix' => 'schema.',
            'expire' => 3600,
        ]);

        $this->db = $container->use(ConnectionManager::class)->build([
            'className' => PostgresConnection::class,
            'host' => getenv('POSTGRES_HOST'),
            'username' => getenv('POSTGRES_USERNAME'),
            'password' => getenv('POSTGRES_PASSWORD'),
            'database' => getenv('POSTGRES_DATABASE'),
            'port' => getenv('POSTGRES_PORT'),
            'charset' => 'utf8',
            'persist' => true,
        ]);

        $this->schema = $container->use(SchemaRegistry::class)->use($this->db);
        $this->cache = $container->use(CacheManager::class)->use('_schema');

        $this->db->query('DROP TABLE IF EXISTS test_values');
        $this->db->query('DROP TABLE IF EXISTS test');

        $this->db->query(<<<'EOT'
            CREATE TABLE test (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY,
                name CHARACTER VARYING(255) NULL DEFAULT NULL,
                value INTEGER NOT NULL DEFAULT 5,
                price NUMERIC(10,2) NOT NULL DEFAULT 2.50,
                text CHARACTER VARYING(255) NOT NULL DEFAULT 'default',
                bool BOOLEAN NOT NULL DEFAULT FALSE,
                date_precision TIMESTAMP(0) NULL DEFAULT NULL,
                created TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
                modified TIMESTAMP WITHOUT TIME ZONE NULL DEFAULT CURRENT_TIMESTAMP,
                PRIMARY KEY (id)
            )
        EOT);
        $this->db->query(<<<'EOT'
            CREATE UNIQUE INDEX name ON test (name)
        EOT);
        $this->db->query(<<<'EOT'
            CREATE INDEX name_value ON test (name, value)
        EOT);
        $this->db->query(<<<'EOT'
            CREATE TABLE test_values (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY,
                test_id INTEGER NOT NULL DEFAULT '0',
                value INTEGER NOT NULL,
                PRIMARY KEY (id)
            )
        EOT);
        $this->db->query(<<<'EOT'
            CREATE INDEX test_values_test_id ON test_values (test_id)
        EOT);
        $this->db->query(<<<'EOT'
            CREATE INDEX value ON test_values (value)
        EOT);
        $this->db->query(<<<'EOT'
            ALTER TABLE test_values ADD CONSTRAINT test_values_test_id FOREIGN KEY (test_id) REFERENCES public.test (id) ON UPDATE CASCADE ON DELETE CASCADE
        EOT);

        @mkdir('tmp');
    }

    #[Override]
    protected function tearDown(): void
    {
        $this->db->query('DROP TABLE IF EXISTS test_values');
        $this->db->query('DROP TABLE IF EXISTS test');

        @unlink('tmp/schema.test.tables');
        @unlink('tmp/schema.test.test.columns');
        @unlink('tmp/schema.test.test.foreign_keys');
        @unlink('tmp/schema.test.test.indexes');
        @unlink('tmp/schema.test.test_values.columns');
        @unlink('tmp/schema.test.test_values.foreign_keys');
        @unlink('tmp/schema.test.test_values.indexes');
        @rmdir('tmp');
    }
}
